# 1. Integer and Float variables
a = 10
b = 3.14

# 2. Addition, Subtraction, Multiplication, Division
print(a + b)       # 13.14
print(a - b)       # 6.86
print(a * b)       # 31.4
print(a / b)       # 3.1847...

# 3. Integer Division and Modulo
print(a // 3)      # 3
print(a % 3)       # 1

# 4. Power
print(2 ** 3)      # 8

# 5. Type Conversion
print(float(a))    # 10.0
print(int(b))      # 3


# 6. Absolute value
print(abs(-5))      # 5

# 7. Round off
print(round(3.456, 2))  # 3.46

# 8. Min and Max
nums = [5, 2, 9, 1]
print(min(nums))     # 1
print(max(nums))     # 9

# 9. Sum
print(sum(nums))     # 17

# 10. Divmod (returns quotient and remainder)
print(divmod(10, 3))  # (3, 1)

# 11. Boolean with numbers
print(bool(0))        # False
print(bool(42))       # True
